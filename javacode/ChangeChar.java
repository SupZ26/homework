public class ChangeChar
{
	public static void main(String[] args)
	{
		System.out.println("北京\t天津\t上海\n");//    \t :  一个制表位，实现对齐的功能
		System.out.println("北京\n天津\n上海\n");//    \n  换行符
		System.out.println("北京\\\\天津\\上海\n");//  \\表示 “\”  \\\\表示“\\”
		System.out.println("他问我:\"这是谁？\"\n");//  \"表示“ 
        System.out.println("我爱你\r中国！");//     \r表示回车
	}

}
